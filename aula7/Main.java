// Estrutura condicional (if-else)

package aula7;

public class Main {
    public static void main(String[] args) {
        int x = 12;

        if (x > 0 && x < 4) {
            System.out.println("Bom dia!");
        } else if (x > 4 && x < 7) {
            System.out.println("Boa tarde!");
        } else {
            System.out.println("Boa noite!");
        }
    }
}
